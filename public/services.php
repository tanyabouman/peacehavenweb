<h1>Long Term Assistance: Group Home/Foster Care </h1>
<p>The goal is to provide facilities where individuals with disabilities who can no longer live with their families can live in a Christian environment that is similar to that of their parental home. The aim is to have a place that will be close enough for regular family visits and attendance at a church that nurtures the individuals with disabilities in the faith of the parental home and church.</p>

